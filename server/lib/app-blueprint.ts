/**
 * Generate a complete AppBlueprint from SchemaContract + design preferences.
 * The blueprint contains every file the generated app needs, organized by dependency layer.
 */
export function contractToBlueprint(input: BlueprintInput): AppBlueprint {
  const features = inferFeatures(input.contract)
  const fileTree: BlueprintFile[] = []

  // Layer 0: Build config + Vite config (overwrite snapshot defaults)
  fileTree.push({
    path: 'vite.config.ts',
    content: generateViteConfig(),
    layer: 0,
    isLLMSlot: false,
  })

  // Layer 1: Schema + CSS + HTML + server infrastructure (all independent)
  fileTree.push({
    path: 'server/db/schema.ts',
    content: contractToDrizzle(input.contract),
    layer: 1,
    isLLMSlot: false,
  })
  fileTree.push({
    path: 'server/db/client.ts',
    content: generateDbClient(),
    layer: 1,
    isLLMSlot: false,
  })
  fileTree.push({
    path: 'server/trpc.ts',
    content: generateTrpcServer(),
    layer: 1,
    isLLMSlot: false,
  })
  fileTree.push({
    path: 'src/index.css',
    content: generateIndexCSS(input.designPreferences),
    layer: 1,
    isLLMSlot: false,
  })
  fileTree.push({
    path: 'index.html',
    content: generateIndexHTML(input.appName),
    layer: 1,
    isLLMSlot: false,
  })
  fileTree.push({
    path: 'src/lib/trpc.ts',
    content: generateTrpcClient(),
    layer: 1,
    isLLMSlot: false,
  })

  // Layer 1 (continued): shadcn/ui components — CRUD pages import from @/components/ui/*
  fileTree.push(...loadUIKit())

  // Layer 2: tRPC routers, root router, .env, SQL migration (depend on schema)
  const routers = contractToTrpc(input.contract)
  for (const router of routers) {
    fileTree.push({
      path: `server/trpc/routers/${router.fileName}`,
      content: router.content,
      layer: 2,
      isLLMSlot: false,
    })
  }
  fileTree.push({
    path: 'server/trpc/router.ts',
    content: contractToRootRouter(input.contract),
    layer: 2,
    isLLMSlot: false,
  })
  fileTree.push({
    path: '.env',
    content: generateDotEnv(),
    layer: 2,
    isLLMSlot: false,
  })
  fileTree.push({
    path: 'drizzle/0001_initial.sql',
    content: contractToSQL(input.contract),
    layer: 2,
    isLLMSlot: false,
  })
  // Layer 3: Root route + Auth guard route — deterministic
  fileTree.push({
    path: 'src/routes/__root.tsx',
    content: generateRootRoute(),
    layer: 3,
    isLLMSlot: false,
  })
  if (features.auth) {
    fileTree.push({
      path: 'src/routes/_authenticated/route.tsx',
      content: `// Auto-generated by VibeStack
import { createFileRoute } from '@tanstack/react-router'
import { AppLayout } from '@/components/app-layout'

export const Route = createFileRoute('/_authenticated')({
  beforeLoad: async () => {
    // Auth check handled by tRPC context — if not authenticated, tRPC returns 401
  },
  component: AppLayout,
})
`,
      layer: 3,
      isLLMSlot: false,
    })
  }

  // Layer 4: Page skeletons (LLM fills JSX bodies)
  const pages = contractToPages(input.contract)
  for (const page of pages) {
    fileTree.push({
      path: `src/routes/_authenticated/${page.fileName}`,
      content: page.content,
      layer: 4,
      isLLMSlot: true,
    })
  }

  // Layer 5: Route tree + wiring files (depend on routes being defined)
  fileTree.push({
    path: 'src/routeTree.gen.ts',
    content: generateRouteTree(input.contract, features),
    layer: 5,
    isLLMSlot: false,
  })
  fileTree.push({
    path: 'src/main.tsx',
    content: generateMainTSX(features),
    layer: 5,
    isLLMSlot: false,
  })
  fileTree.push({
    path: 'src/components/app-layout.tsx',
    content: generateAppLayout(input.appName, features),
    layer: 5,
    isLLMSlot: false,
  })

  return {
    meta: {
      appName: input.appName,
      appDescription: input.appDescription,
      designPreferences: input.designPreferences,
    },
    features,
    contract: input.contract,
    fileTree,
  }
}
